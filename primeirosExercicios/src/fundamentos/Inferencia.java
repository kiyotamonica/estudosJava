package fundamentos;

public class Inferencia {
	public static void main(String[] args) {
		double variavel1 = 4.5;
		System.out.println(variavel1);
		
		var variavel2 = 4.5;
		System.out.println(variavel2);
		
		 /*Em java, é possível utilizar var para declarar uma
		variável, no entanto, vai ocorrer a inferencia. De
		acordo com o conteúdo da variavel, o tipo dela será
		inferido automaticamente.*/
	}
}
